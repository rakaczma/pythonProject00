a = int(input("Wprowadż pierwszą liczbę całkowita: "))
b = int(input("Wprowadż drugą liczbę całkowita: "))

dodawanie = a + b
print("Wynik dodawania:", dodawanie)

odejmowanie = a - b
print("Wynik odejmowania:", odejmowanie)

mnozenie = a * b
print("Wynik mnozenia:", mnozenie)

dzielenie = a / b
print("Wynik dzielenie:", dzielenie)

modulo = a % b
print("Wynik modulo:", modulo)